    parser.add_argument("--base-url", default="http://localhost:8080", help="URL base da API")
    parser.add_argument("--images-dir", default="./images", help="Diretório com as imagens")
    parser.add_argument("--json-file", default="./seed_articulation_types.json", help="Arquivo JSON de seed")
    parser.add_argument("--token", default=None, help="Token de autenticação (ex: 'Bearer eyJ...')")
    parser.add_argument("--dry-run", action="store_true", help="Apenas simula, sem fazer requests")
    parser.add_argument("--delay", type=float, default=0.2, help="Delay entre requests em segundos")

    args = parser.parse_args()

    # ─── Validações ──────────────────────────────────────────────────────────
    json_path = Path(args.json_file)
    images_dir = Path(args.images_dir)

    images_dir = images_dir / "manual_gonimetria"

    # ─── Headers ─────────────────────────────────────────────────────────────
    headers = {
        "Content-Type": "application/json",
        "Accept": "application/json",
    }
    if args.token:
        token = args.token if args.token.startswith("Bearer ") else f"Bearer {args.token}"
        headers["Authorization"] = token

