# ─── Cache de imagens já enviadas (evita reenvio da mesma figura) ────────────
uploaded_images_cache: dict[str, dict] = {}


def upload_image(base_url: str, image_path: Path, headers: dict, dry_run: bool = False) -> dict:
    """
    Faz upload de uma imagem via POST /api/files (multipart/form-data).
    Retorna o objeto FileStorageDto com o 'id' do arquivo.
    """
    file_name = image_path.name

    # Verifica cache para não reenviar a mesma imagem
    if file_name in uploaded_images_cache:
        print(f"  ⏩ Imagem já enviada (cache): {file_name}")
        return uploaded_images_cache[file_name]

    if dry_run:
        fake_id = f"fake-uuid-{file_name}"
        result = {"id": fake_id, "fileName": file_name}
        uploaded_images_cache[file_name] = result
        print(f"  🔸 [DRY-RUN] Upload: {file_name} → id={fake_id}")
        return result

    if not image_path.exists():
        print(f"  ⚠️  Imagem não encontrada: {image_path}. Criando placeholder...")
        # Cria uma imagem placeholder (1x1 pixel PNG transparente)
        image_path.parent.mkdir(parents=True, exist_ok=True)
        # PNG 1x1 transparente mínimo
        placeholder = bytes([
            0x89, 0x50, 0x4E, 0x47, 0x0D, 0x0A, 0x1A, 0x0A,  # PNG signature
            0x00, 0x00, 0x00, 0x0D, 0x49, 0x48, 0x44, 0x52,  # IHDR chunk
            0x00, 0x00, 0x00, 0x01, 0x00, 0x00, 0x00, 0x01,
            0x08, 0x06, 0x00, 0x00, 0x00, 0x1F, 0x15, 0xC4,
            0x89, 0x00, 0x00, 0x00, 0x0A, 0x49, 0x44, 0x41,
            0x54, 0x78, 0x9C, 0x62, 0x00, 0x00, 0x00, 0x02,
            0x00, 0x01, 0xE5, 0x27, 0xDE, 0xFC, 0x00, 0x00,
            0x00, 0x00, 0x49, 0x45, 0x4E, 0x44, 0xAE, 0x42,
            0x60, 0x82
        ])
        with open(image_path, "wb") as f:
            f.write(placeholder)

    content_type = mimetypes.guess_type(str(image_path))[0] or "image/png"

    url = f"{base_url}/api/files"

    # Remove Content-Type do header para o requests definir o boundary do multipart
    upload_headers = {k: v for k, v in headers.items() if k.lower() != "content-type"}

    with open(image_path, "rb") as f:
        files = {
            "file": (file_name, f, content_type)
        }
        data = {
            "description": f"Imagem goniometria: {file_name}",
            "storageMethod": "DATABASE"
        }

        print(f"  📤 Enviando imagem: {file_name} ...", end=" ", flush=True)
        response = requests.post(url, headers=upload_headers, files=files, data=data)

    if response.status_code != 200:
        print(f"❌ Erro ({response.status_code})")
        print(f"     Response: {response.text[:500]}")
        raise Exception(f"Falha no upload de {file_name}: HTTP {response.status_code}")

    result_json = response.json()

    # O endpoint retorna Content<FileStorageDto>, então o DTO fica em "data"
    file_dto = result_json.get("data", result_json)

    print(f"✅ id={file_dto.get('id')}")

    uploaded_images_cache[file_name] = file_dto
    return file_dto


def build_articulation_payload(
        articulation: dict,
        base_url: str,
        images_dir: Path,
        headers: dict,
        dry_run: bool = False
) -> dict:
    """
    Monta o payload ArticulationTypeCreateOrUpgradeDto a partir do JSON de seed.
    Faz upload das imagens referenciadas e substitui pelo FileStorageDto com id.
    """
    movements_payload = []

    for movement in articulation.get("movementsTypes", []):
        figure_name = movement.get("figureName")
        image_path = images_dir / figure_name if figure_name else None

        # Upload da imagem
        file_dto = None
        if image_path and figure_name:
            file_dto = upload_image(base_url, image_path, headers, dry_run)

        movement_dto = {
            "id": movement.get("id"),  # None para criação
            "type": movement["type"],
            "description": movement["description"],
            "image": {"id": file_dto["id"]} if file_dto else None,
            "angleRule": movement.get("angleRule"),
            "goniometryProcedure": movement.get("goniometryProcedure"),
        }
        movements_payload.append(movement_dto)

    payload = {
        "id": articulation.get("id"),  # None para criação
        "type": articulation["type"],
        "description": articulation["description"],
        "side": articulation.get("side", "RIGHT"),
        "movementsTypes": movements_payload,
    }

    return payload


def save_articulation_type(base_url: str, payload: dict, headers: dict, dry_run: bool = False) -> dict | None:
    """
    Envia o payload para POST /api/articulation-types.
    """
    url = f"{base_url}/api/articulation-types"

    if dry_run:
        print(f"  🔸 [DRY-RUN] POST {url}")
        print(f"     Payload: {json.dumps(payload, ensure_ascii=False, indent=2)[:800]}...")
        return None

    print(f"  📨 Salvando articulação: {payload['type']} ({payload['side']}) ...", end=" ", flush=True)

    response = requests.post(url, headers=headers, json=payload)

    if response.status_code != 200:
        print(f"❌ Erro ({response.status_code})")
        print(f"     Response: {response.text[:500]}")
        raise Exception(f"Falha ao salvar articulação: HTTP {response.status_code}")

    result_json = response.json()
    data = result_json.get("data", result_json)
    print(f"✅ id={data.get('id')}")
    return data


